package by.intexsoft.malkevich;

public class MyBox {

	private int width;
	private int height;
	private int depth;

	MyBox() {
		width = 10;
		height = 10;
		depth = 10;
	}

	MyBox(int w, int h, int d) {
		width = w;
		height = h;
		depth = d;
	}

	MyBox(int a) {
		width = a;
		height = a;
		depth = a;
	}
}
